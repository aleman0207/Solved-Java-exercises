public class TipoVar {
    public static void main(String[] args) {

        System.out.println("*** Tipo var en Java *** ");

        // Sin uso

        String nombre = "luis";

        System.out.println("Nombre = " + nombre);

        // Con uso de Var

        var nombre2 = "Carlos";

        System.out.println();
        System.out.println("Nombre = " + nombre2);

    }
}
