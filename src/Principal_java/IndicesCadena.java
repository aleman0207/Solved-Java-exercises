package Principal_java;

public class IndicesCadena  {

    public static void main(String[] args) {

        // Manejo de indices de cadena

        String cadena1 ="Hola mundo";


        // Recuperar caracter

        char primerCaracter = cadena1.charAt(0);
        System.out.println("primerCaracter = " + primerCaracter);
        char letraM = cadena1.charAt(5);
        System.out.println("letraM = " + letraM);

        char letraD = cadena1.charAt(8);
        System.out.println("letraD = " + letraD);


    }
}
